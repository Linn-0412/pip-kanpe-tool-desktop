export function normalizeLanguage(language) {
  const normalized = getLanguageCode(language);
  return SUPPORTED_LANGUAGES.some((entry) => entry.code === normalized) ? normalized : DEFAULT_LANGUAGE;
}

export function getPreferredLanguage(languages = globalThis.navigator?.languages ?? [globalThis.navigator?.language]) {
  const preferred = languages
    .map((language) => getLanguageCode(language))
    .find((language) => SUPPORTED_LANGUAGES.some((entry) => entry.code === language));
  return preferred ?? DEFAULT_LANGUAGE;
}

export function translate(language, key, params = {}) {
  const template = getTranslationValue(language, key) ?? getTranslationValue(DEFAULT_LANGUAGE, key) ?? key;
  return interpolate(template, params);
}

export function applyDocumentTranslations(root, language) {
  const currentLanguage = normalizeLanguage(language);
  root.documentElement.lang = currentLanguage;

  STATIC_TRANSLATIONS.forEach(({ selector, attr = "textContent", key }) => {
    root.querySelectorAll(selector).forEach((element) => {
      const value = translate(currentLanguage, key);
      if (attr === "textContent") {
        element.textContent = value;
      } else {
        element.setAttribute(attr, value);
      }
    });
  });
}

function getTranslationValue(language, key) {
  return key.split(".").reduce((current, part) => current?.[part], TRANSLATIONS[normalizeLanguage(language)]);
}

function getLanguageCode(language) {
  return `${language ?? ""}`.toLowerCase().split("-")[0];
}

function interpolate(template, params) {
  return `${template}`.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key) =>
    Object.prototype.hasOwnProperty.call(params, key) ? `${params[key]}` : match,
  );
}
